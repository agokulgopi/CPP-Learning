#include <iostream>
using namespace std;
typedef int price;
typedef int quantity;
int main() {
    price p1 = 100, p2 = 200;
    quantity q1 = 5, q2 = 10;

    cout<<"Price 1: "<<p1<<endl; // Output: 100
    cout<<"Price 2: "<<p2<<endl; // Output: 200
    cout<<"Quantity 1: "<<q1<<endl; // Output: 5
    cout<<"Quantity 2: "<<q2<<endl; // Output: 10
    return 0;
}